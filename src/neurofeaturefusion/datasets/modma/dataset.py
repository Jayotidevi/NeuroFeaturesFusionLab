from neurofeaturefusion.core.base_dataset import BaseDataset


class MODMADataset(BaseDataset):
    """
    Dataset implementation for MODMA.

    Stores MODMA samples generated by the parser.
    """

    def __init__(
        self,
        name="MODMA",
        config=None
    ):
        super().__init__(
            name=name,
            config=config
        )

        self.samples = []


    def add_sample(self, sample):
        """
        Add a BaseSample object.
        """
        self.samples.append(sample)


    def __len__(self):
        """
        Return number of samples.
        """
        return len(self.samples)


    def __getitem__(self, index):
        """
        Return one sample.
        """
        return self.samples[index]


    def validate(self):
        """
        Validate dataset structure.
        """
        if not isinstance(self.samples, list):
            raise TypeError(
                "Samples must be stored as a list."
            )

        return True
